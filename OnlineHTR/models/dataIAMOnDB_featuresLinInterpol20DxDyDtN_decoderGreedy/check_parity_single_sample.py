MODEL_PATH = "/home/sundeep/dev/tldraw-handwriting-rec-plugin-obsidian/OnlineHTR/models/dataIAMOnDB_featuresLinInterpol20DxDyDtN_decoderGreedy/model.onnx"
ALPHABET_PATH = "/home/sundeep/dev/tldraw-handwriting-rec-plugin-obsidian/OnlineHTR/models/dataIAMOnDB_featuresLinInterpol20DxDyDtN_decoderGreedy/alphabet.json"

def load_alphabet(path):
    data = json.load(open(path, "r", encoding="utf-8"))
    if isinstance(data, dict):
        return data["alphabet"]
    return data

def decode_greedy_ctc(logits_tnc, alphabet, blank_index=0):
    # logits_tnc shape: [T, N, C], N should be 1
    t, n, c = logits_tnc.shape
    assert n == 1, f"Expected batch=1, got {n}"
    assert len(alphabet) + 1 == c, f"alphabet/classes mismatch: len(alphabet)={len(alphabet)}, classes={c}"

    seq = np.argmax(logits_tnc[:, 0, :], axis=1).tolist()

    collapsed = []
    prev = None
    for idx in seq:
        if idx != prev:
            collapsed.append(idx)
            prev = idx

    filtered = [idx for idx in collapsed if idx != blank_index]
    text = "".join(alphabet[idx - 1] if 0 <= idx - 1 < len(alphabet) else "" for idx in filtered)
    return text

def main():
    alphabet = load_alphabet(ALPHABET_PATH)

    ink = np.array(sample["ink"], dtype=np.float32)
    T = sample["timeSteps"]
    C = sample["channels"]
    x = ink.reshape(T, 1, C)  # [T,1,4]

    sess = ort.InferenceSession(MODEL_PATH, providers=["CPUExecutionProvider"])
    in_name = sess.get_inputs()[0].name
    out_name = sess.get_outputs()[0].name

    y = sess.run([out_name], {in_name: x})[0]  # expected [T,1,classes]
    py_pred = decode_greedy_ctc(y, alphabet, blank_index=0)

    print("Plugin prediction:", sample["prediction"])
    print("Python prediction:", py_pred)
    print("Match:", py_pred == sample["prediction"])
    print("Output shape:", y.shape)
    print("Input/Output names:", in_name, out_name)

if __name__ == "__main__":
    main()
